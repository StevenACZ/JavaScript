// Valores objetos
let nulo = null;
let arr = [1,2,3];
let obj = {
  1:1,
  2:2,
  3:3
};

console.log('> Valores objetos')
console.log(typeof nulo, nulo);
console.log(typeof arr, arr);
console.log(typeof obj, obj);