// Valores primitivos
let number = 40;
let string = 'Steven';
let boolean = true;
let indefinido = undefined;

console.log('> Valores primitivos')
console.log(typeof number, number);
console.log(typeof string, string);
console.log(typeof boolean, boolean);
console.log(typeof indefinido, undefined, '\n');

